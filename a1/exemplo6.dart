void main() {
  var diaSemana = 'Segunda-feira';

  switch (diaSemana) {
    case 'Segunda-feira':
      print('Hoje é segunda-feira.');
      break;
    case 'Terça-feira':
      print('Hoje é terça-feira.');
      break;
    case 'Quarta-feira':
      print('Hoje é quarta-feira.');
      break;
    default:
      print('Dia da semana inválido.');
  }
}
